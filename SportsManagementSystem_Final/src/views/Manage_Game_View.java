    private boolean validateAddGameForm(String game_name, String game_players, String game_duration){
        if(game_name.equals("")){
            this.showMsg("Game Name is empty");
            return false;
        }
        if(game_players.equals("")){
            this.showMsg("No. of Players is empty");
            return false;
        }
        if(game_duration.equals("")){
            this.showMsg("Time Duration is empty");
            return false;
        }
        return true;
    }
    
    private boolean validateUpdateGameForm(String game_players, String game_duration){
        if(game_players.equals("")){
            this.showMsg("No. of Players is empty");
            return false;
        }
        if(game_duration.equals("")){
            this.showMsg("Time Duration is empty");
            return false;
        }
        return true;
    }
    
    private void paneChangedSetUI(int index) {
        switch(index){
            case 0:
                add_game_name.setText("");
                add_game_players.setText("");
                add_game_duration.setText("");
                break;
                
            case 1:
                List<Game_Model> games1 = controller.getAllGames();
                if(!games1.isEmpty()){
                    update_game_name.removeAllItems();
                    for(Game_Model game: games1){
                        update_game_name.addItem(game);
                    }
                }
                break;
                
            case 2:
                List<Game_Model> games2 = controller.getAllGames();
                if(!games2.isEmpty()){
                    del_game_name.removeAllItems();
                    for(Game_Model game: games2){
                        del_game_name.addItem(game);
                    }
                }
                break;
                
            case 3:
                List<Game_Model> games3 = controller.getAllGames();
                if(!games3.isEmpty()){
                    view_students.removeAllItems();
                    for(Game_Model game: games3){
                        view_students.addItem(game);
                    }
                }
                break;
        }
    }
    
