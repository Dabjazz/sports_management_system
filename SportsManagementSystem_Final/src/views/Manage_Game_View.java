    private void viewStudentsSetUI(){
        Game_Model selectedGame3 = (Game_Model)view_students.getSelectedItem();
        if(selectedGame3 == null){
            return;
        }   
        
        String gname = selectedGame3.getGame_name();
        List<User_Model> stud_games = User_Model.getAllStudentGamesDetails(gname);
   
        DefaultTableModel aModel = new DefaultTableModel() {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };
        Object[] tableColumnNames = new Object[2];
        tableColumnNames[0] = "Student ID";
        tableColumnNames[1] = "Student Name";
        aModel.setColumnIdentifiers(tableColumnNames);
        Object[] objects = new Object[2];
        if(!stud_games.isEmpty()){
            for(User_Model stud_game: stud_games)
            {
                objects[0] = stud_game.getUser_id();
                objects[1] = stud_game.getUser_first_name()+" "+stud_game.getUser_last_name();
                aModel.addRow(objects);
            }
        }
        tbl_view_students.setModel(aModel);
        tbl_view_students.repaint();
    }
    
