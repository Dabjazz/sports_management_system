    private void btn_add_gameActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_add_gameActionPerformed
        // TODO add your handling code here:
        String game_name = add_game_name.getText();
        String game_players = add_game_players.getText();
        String game_duration = add_game_duration.getText();
        
        int game_no_of_players = Integer.parseInt(game_players);
        int game_time_duration = Integer.parseInt(game_duration);
        
        if(this.validateAddGameForm(game_name, game_players, game_duration)){
            controller.addGame(game_name, game_no_of_players, game_time_duration);
        }
        add_game_name.setText("");
        add_game_players.setText("");
        add_game_duration.setText("");
    }//GEN-LAST:event_btn_add_gameActionPerformed

